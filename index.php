	<section class="faq background-blanco font-celeste" id="faq">
		<div class="title-faq">
			<img src="img/faq-icon.png" alt="FAQ">
			<h1>Preguntas frecuentes</h1>
		</div>
		<article class="content-faq">
			<div>
				<h3>¿Cuáles son los medios de pago aceptados para abonar el seguro?</h3>
				<p class="font-grey">Se puede abonar mediante tarjeta de crédito, tarjeta de débito, débito automático por CBU, RapiPago o Pagofácil.</p>
			</div>
			<div>
				<h3>¿Qué documentación debo tener para poder circular con mi vehículo?</h3>
				<p class="font-grey">Es importante que tengas el registro de conducir vigente, la cédula verde o azul, según corresponda, patente al día, la Verificación Técnica Vehicular si corresponde, el comprobante del seguro que te llega en la póliza y, en caso de poseer equipo de gas, tenés que contar con la cédula de identificación del mismo, con su adecuada oblea.<br>Para viajar a cualquier país del MERCOSUR tenés que agregar a lo mencionado el certificado de MERCOSUR que te enviamos junto a tu póliza.</p>
			</div>
			<div>
				<h3>Tuve un siniestro con el vehículo, ¿cómo debo proceder?</h3>
				<p class="font-grey">Tené en cuenta que si en un choque contra un tercero hubo lesionados, la policía intervendrá, por lo que tendrás que realizar la denuncia policial pertinente.<br>Caso contrario, lo único que tenés que hacer en el momento es solicitar al tercero todos los datos personales y del seguro, es decir: patente, modelo del auto, año, compañía de seguros, número de póliza, datos de la Cédula verde, datos del registro de conducir, nombre y apellido, teléfono de contacto y DNI.<br>Luego, dentro de las 72 horas comunicate con nuestra oficina para realizar la denuncia administrativa.</p>
			</div>
			<div>
				<h3>¿Qué documentación debo presentar en caso de siniestro?</h3>
				<ul class="item-list">
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Licencia de conducir.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Tarjeta verde.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Fotos del vehiculo.</li>
				</ul>
			</div>
			<div>
				<h3>Si me choca un tercero sin seguro o sin registro, ¿puedo reclamar?</h3>
				<p class="font-grey">Cuando una persona maneja un vehículo sin el seguro obligatorio de Responsabilidad Civil está cometiendo una infracción. En este caso no hay compañía de seguros a la que reclamar, por lo que tenés que hacer el reclamo directamente a la persona que te chocó.<br>Si no tiene registro de conducir también está cometiendo una infracción, por lo que su compañía de seguros podría desistir de responder por su siniestro. De todos modos siempre debés hacer el reclamo en la compañía del tercero y esperar a que la misma se expida.</p>
			</div>
			<div>
				<h3>¿En que casos la compañía puede rechazar un siniestro?</h3>
				<p class="font-grey">Entre los más importantes:<p>
				<ul class="item-list">
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Si la póliza está impaga.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Si la cobertura no está incluida, por ejemplo, daños parciales.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Si el conductor no está habilitado para conducir esa categoría de vehículo o tenga el registro vencido.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Si el automóvil tiene un uso distinto al que figura en la póliza de seguro.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Si los daños son ocasionados por dolo o culpa grave del asegurado o del conductor</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Cuando el vehículo no se encuentra habilitado para circular conforme a las disposiciones legales vigentes.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Cuando el conductor maneja bajo la influencia de cualquier droga des inhibidora, alucinógena o somnífera, o en estado de ebriedad.</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Cuando no se respeten las normas de tránsito. (Barreras ferroviarias, exceso de velocidad, transitar en contramano, etc).</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Por falta de denuncia (caducidad).</li>
					<li class="font-grey"><span class="icon-list font-celeste">•</span>Por reticencia o falsedad en cualquier información dada.</li>
				</ul>
			</div>
